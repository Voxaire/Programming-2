package Basics;

public class forLoops {
    public static void main(String[] args) {
        // For loops will repeat themselves until the condition is false.
        // The first argument sets up a variable, the 2nd argument is the condition the for loop checks for. The third argument is what the loop will do when each time it ends.
        for (int i = 1; i <= 10; i++) {
            System.out.println("The Number is now: " + i);
        }
    }
}
