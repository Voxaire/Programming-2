package Object_Oriented_Programming;

public class Objects {
    int x = 5;
    int y = 6;
    int z = 8;
    public static void main(String[] args)  {
        Objects myObj = new Objects();
        System.out.println(myObj.x);
        System.out.println(myObj.y);
        System.out.println(myObj.z);
    }
}
